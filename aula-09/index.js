/*=============== DIFERENÇA ENTRE VAR E LET ================*/

nome = 'Luiz'; // NÃO CRIE VARIAVEL SEM CONST OU LET

/*======  LET  ========*/
let nome ='João';
let nome = 'Otávio'; /* NÃO PODE REDECLARAR VARIAVEL*/

/* para alterar o valor da variavel utilize */
nome = 'Otávio';

/*========== VAR ==========*/
var nome = 'Maicon';
var nome = 'Rodolfo'; // pode redeclarar 

